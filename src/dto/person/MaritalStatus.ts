/**
 * Person marital status
 * 个人婚姻状况
 */
export enum MaritalStatus {
  /**
   * Single
   * 未婚
   */
  Single = 1,

  /**
   * Married
   * 已婚
   */
  Married = 2,

  /**
   * Partnership
   * 同居伴侣
   */
  Partnership = 3,

  /**
   * Separated
   * 分居
   */
  Separated = 4,

  /**
   * Divorced
   * 离异
   */
  Divorced = 5,

  /**
   * Widowed
   * 丧偶
   */
  Widowed = 6
}
